#include <stdio.h>
/**
 * main - Entry point
 *
 * Return: Always 0 (Success)  
 */ 
int main(void)
{
	int i;
	int j;
	int k;
	int l;

	for (j = 48; j < 58; j++);
	{
		putchar(i);
		putchar(j);
		putchar(' ');
		putchar(',');
	}
	for (k = 48; k < 58; k++)
	{
		for (l = 48; l < 58; l++)
		{
			putchar(k);
			putchar(l);
			putchar(' ');
			putchar(',');
		}
	}
	return (0);
}

